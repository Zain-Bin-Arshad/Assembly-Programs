
; FOLOWING CODE PRINTS * IN DIRCTION OF ARROWS KEYS
; AND RESET THE SCREEN FOR OR CLEAR SCREEN BY SPACE KEY

PRINT MACRO
   MOV AH,2
   INT 10H 
   MOV AH,9
   MOV AL,'*'
   MOV BH,0
   MOV BL,00000111B
   MOV CX,1
   INT 10H
ENDM    

ORG 100H
.DATA

.CODE
MAIN PROC 

START:    
   MOV AH,2
   MOV DH,12
   MOV DL,39  ;MOV CURSOR TO CENTER
   MOV BH,0
   INT 10H
   
   MOV AH,1
   MOV CH,0    ; CHANGES CURSOR SCAN LINE
   MOV CL,14
   INT 10H
               ; MACRO TO PRINT CHARACTER
   PRINT
   
   MOV AH,0
   INT 16H

WHILE:   
   CMP AL,1BH   ; ESCAPE
   JE CLOSE
   CMP AH,48H   ; UP ARROW
   JE UP
   CMP AH,50H   ;DOWN ARROW
   JE DOWN
   CMP AH,4DH   ;RIGHT ARROW
   JE RIGHT
   CMP AH,4BH   ;LEFT ARROW
   JE LEFT
   CMP AL,20H   ;SPACE
   JE CLEAR 
   JMP TAKE
    
UP: 
   CMP DH,0
   JE SCROLL_DOWN 
   DEC DH 
   PRINT
   JMP TAKE

SCROLL_DOWN:
   MOV AL,1
   XOR CX,CX
   MOV DX,184FH
   MOV AH,7
   INT 10H
    
   PRINT
   JMP TAKE
    
DOWN:
   INC DH
   PRINT
   JMP TAKE
   
RIGHT:
   INC DL
   PRINT
   JMP TAKE
     
LEFT:
   DEC DL
   PRINT
   JMP TAKE
   
CLEAR:
   MOV AH,6
   XOR AL,AL
   XOR CX,CX 
   MOV DX,184FH    ; CLEARING SCREEN
   INT 10H
   JMP START
   
TAKE:           
   MOV AH,0
   INT 16H
   JMP WHILE

CLOSE:
    RET  
MAIN ENDP



